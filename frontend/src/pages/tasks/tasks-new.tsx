import { RichTextField } from '../../components/RichTextField';

import { create } from '../../stores/tasks/tasksSlice';
import { useAppDispatch } from '../../stores/hooks';
import { useRouter } from 'next/router';
import moment from 'moment';

const initialValues = {
  title: '',

  description: '',

  hardness_level: 'easy',

  deadline: '',

  urgency_level: 'urgent',

  point_value: '',

  user: '',
};

const TasksNew = () => {
  const router = useRouter();
  const dispatch = useAppDispatch();

  const handleSubmit = async (data) => {
    await dispatch(create(data));
    await router.push('/tasks/tasks-list');
  };
  return (
    <>
      <Head>
        <title>{getPageTitle('New Item')}</title>
      </Head>
      <SectionMain>
        <SectionTitleLineWithButton
          icon={mdiChartTimelineVariant}
          title='New Item'
          main
        >
          {''}
        </SectionTitleLineWithButton>
        <CardBox>
          <Formik
            initialValues={initialValues}
            onSubmit={(values) => handleSubmit(values)}
          >
            <Form>
              <FormField label='Title'>
                <Field name='title' placeholder='Title' />
              </FormField>

              <FormField label='Description' hasTextareaHeight>
                <Field
                  name='description'
                  as='textarea'
                  placeholder='Description'
                />
              </FormField>

              <FormField label='HardnessLevel' labelFor='hardness_level'>
                <Field
                  name='hardness_level'
                  id='hardness_level'
                  component='select'
                >
                  <option value='easy'>easy</option>

                  <option value='medium'>medium</option>

                  <option value='hard'>hard</option>
                </Field>
              </FormField>

              <FormField label='Deadline'>
                <Field
                  type='datetime-local'
                  name='deadline'
                  placeholder='Deadline'
                />
              </FormField>

              <FormField label='UrgencyLevel' labelFor='urgency_level'>
                <Field
                  name='urgency_level'
                  id='urgency_level'
                  component='select'
                >
                  <option value='urgent'>urgent</option>

                  <option value='non-urgent'>non-urgent</option>
                </Field>
              </FormField>

              <FormField label='PointValue'>
                <Field
                  type='number'
                  name='point_value'
                  placeholder='PointValue'
                />
              </FormField>

              <FormField label='User' labelFor='user'>
                <Field
                  name='user'
                  id='user'
                  component={SelectField}
                  options={[]}
                  itemRef={'users'}
                ></Field>
              </FormField>

              <BaseDivider />
              <BaseButtons>
                <BaseButton type='submit' color='info' label='Submit' />
                <BaseButton type='reset' color='info' outline label='Reset' />
                <BaseButton
                  type='reset'
                  color='danger'
                  outline
                  label='Cancel'
                  onClick={() => router.push('/tasks/tasks-list')}
                />
              </BaseButtons>
            </Form>
          </Formik>
        </CardBox>
      </SectionMain>
    </>
  );
};

TasksNew.getLayout = function getLayout(page: ReactElement) {
  return (
    <LayoutAuthenticated permission={'CREATE_TASKS'}>
      {page}
    </LayoutAuthenticated>
  );
};

export default TasksNew;
